public class Client {
    private String name;
    private String cpf;
    private String job;

    public String getName () {
        return this.name;
    }

    public void setName (String name) {
        this.name = name;
    }

    public String getCPF () {
        return this.cpf;
    }

    public void setCPF (String cpf) {
        this.cpf = cpf;
    }

    public String getJob () {
        return this.job;
    }

    public void setJob (String job) {
        this.job = job;
    }
}
