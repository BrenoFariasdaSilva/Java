public class TestCharacters {
    public static void main (String[] args) {
        char character = 'B';
        System.out.println(character);

        character = 66;
        System.out.println(character);

        character = (char) (character + 1);
        System.out.println(character);

        String word = "My name is ";
        System.out.println(word);

        word = "My name is " + "Breno";
        System.out.println(word);
    }
}
