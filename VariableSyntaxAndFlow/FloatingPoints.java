public class FloatingPoints {
    public static void main (String[] args) {
        double salary = 1250.50;

        System.out.println ("My salary is: " + salary);

        double age = 20;

        System.out.println (age);

        double division = 3.14 / 2;

        System.out.println (division);

        int newDivision = 5 / 2;

        System.out.println (newDivision);

        double newAttempt = 5.0 / 2;

        System.out.println (newAttempt);

    }
}
